class Book {
  constructor(title, author, publisher, cover, pubdate, isbn, seriesName, categoryName, link) {
    this.title = title
    this.author = author
    this.publisher = publisher
    this.cover = cover
    this.pubdate = pubdate
    this.isbn = isbn
    this.seriesName = seriesName
    this.categoryName = categoryName
    this.link = link
  }
}

export default Book
